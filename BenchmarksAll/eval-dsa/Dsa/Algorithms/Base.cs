namespace Dsa.Algorithms
{
    /// <summary>
    /// Number bases.
    /// </summary>
    public enum Base
    {
        /// <summary>
        /// Binary. Base 2.
        /// </summary>
        Binary = 2,

        /// <summary>
        /// Octal. Base 8.
        /// </summary>
        Octal = 8,

        /// <summary>
        /// Decimal. Base 10.
        /// </summary>
        Decimal = 10,

        /// <summary>
        /// Hexadecimal. Base 16.
        /// </summary>
        Hexadecimal = 16
    }
}